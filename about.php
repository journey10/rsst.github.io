        <h3 class="about-title">The Academe</h3>
        <p>Renaissance School of Science & Technology, RSST (formerly The PRISMS) is the product of years of work, study, planning and experience by many individuals. The idea to create a quality preparatory, elementary, secondary and Technical Vocational Two (2) years Ladderized Courses in Morong, Rizal began as a response to the community's increasing demand for quality education with emphasis on modern Science, Mathematics, Development in Education Values, Information Technology, business-related training and global changes that require school children and adult to be universally competitive to innovated system or curricular offerings, methods of teaching strategies in line with rigorous national and informational academic standards for adult learners who are either changing careers or in need of an educational update in the areas of computers, business planning, accounting, sales or administrative skills. Research showed that people with strong skills in these areas could expect better pay than "entry level" salaries in a variety of jobs. So the curriculum was designed to train students in these areas as well as prepare those interested in principles planning and development.
    <br>
        The school was incorporated in September 2004 and officially opened in May 2005 offering only preparatory and elementary education. After extensive research of community needs and demand for secondary and vocational courses, plans were framed for expanded secondary level, business and computer programs for day and evening courses. THE PRISMS was then amended to Renaissance School of Science & Technology Inc. (RSST Inc.) in the year 2006.</p>
